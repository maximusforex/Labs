public class Bitwise {
    public static void main(String[] args){
        int a = 24;
        int b = 12;
        int res = a&b;
        System.out.println(res);
        res = a|b;
        System.out.println(res);
        res = a^b;
        System.out.println(res);
        res = ~a;
        System.out.println(res);
        res = ~b;
        System.out.println(res);
    }
}
